         );


            return cardConfig;
        }

    }
    [EntityLogic(typeof(FairySwarmDef))]
    public sealed class FairySwarm : ClownCard
    {
        protected override IEnumerable<BattleAction> Actions(UnitSelector selector, ManaGroup consumingMana, Interaction precondition)
        {
            yield return AttackAction(selector.SelectedEnemy);
            foreach (Card card in this.Battle.HandZone.Where<Card>((Func<Card, bool>)(card => card.CardType == CardType.Friend)).ToList<Card>())
            {
                card.NotifyActivating();
                card.Loyalty += Value1;
            }
        }
    }
